package com.kidskart.app.pojo;

public class UserDetails {

	private String name;
	private int age;

	public UserDetails() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
